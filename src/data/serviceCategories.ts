// Mapping for legacy category IDs stored in old Firestore documents
export const LEGACY_CATEGORY_MAP: Record<string, string> = {
  acompanhante: 'saude_cuidados',
  adestramento: 'pet_animais',
  buffet: 'festas_eventos',
  chaveiro: 'manutencao_residencial'
};

export function getCategoryById(id: string): CategoryDefinition | undefined {
  const mappedId = LEGACY_CATEGORY_MAP[id] || id;
  return SERVICE_CATEGORIES.find(c => c.id === mappedId || c.id === id);
}

export function getCategoryNameById(id: string): string {
  if (id === 'todos') return 'Todos os Serviços';
  const cat = getCategoryById(id);
  if (cat) return cat.name;
  
  // Backward compatibility fallback names
  if (id === 'acompanhante') return 'Acompanhante de Idoso';
  if (id === 'adestramento') return 'Adestramento de Pets';
  if (id === 'buffet') return 'Buffet e Eventos';
  if (id === 'chaveiro') return 'Chaveiro';

  return id;
}

export function getCategoryColorById(id: string): string {
  const cat = getCategoryById(id);
  return cat ? cat.colorClass : 'bg-stone-100 text-stone-800 border-stone-200';
}
